import java.util.ArrayList;

public class FriendsList {

    public static void main(String[] args) {
        
        // Create an ArrayList to store names of friends
        ArrayList<String> friends = new ArrayList<>();
        
        // Add 5 names to the list
        friends.add("Alice");
        friends.add("Bob");
        friends.add("Charlie");
        friends.add("David");
        friends.add("Emma");
        
        // Print the list of friends
        System.out.println("Friends List: " + friends);
    }
}